import UIKit

extension UIButton {
    func setSelectedColor(){
        self.backgroundColor = #colorLiteral(red: 0.5244179077, green: 0.8300867691, blue: 0.9793455005, alpha: 1)
    }
    func setDiselectedColor(){
        self.backgroundColor = #colorLiteral(red: 0.5254901961, green: 0.831372549, blue: 0.9803921569, alpha: 0.38)
    }
}
